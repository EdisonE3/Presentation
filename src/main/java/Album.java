public class Album {
    int rowId;
    String title;
    String artist;
    boolean isClassical;
    String composer;

    public Album(int rowId, String title, String artist, boolean isClassical, String composer) {
        this.rowId = rowId;
        this.title = title;
        this.artist = artist;
        this.isClassical = isClassical;
        this.composer = composer;
    }


}
